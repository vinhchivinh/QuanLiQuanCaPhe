    public void duaDuLieuVaoTable() {
        String a = "Bàn Số ";
        String column[] = {"ID", "SỐ BÀN"};
        String data[][] = new String[1000][1000];
        List<Ban> Danh_sach_ban = new ArrayList<>();
        Danh_sach_ban = svban.danhSachBan();
        int i = 0, j = 0;
        for (Ban value : Danh_sach_ban) {
            data[i][j] = Integer.toString(value.getId_ban());
            data[i][j + 1] = a.concat(Integer.toString(value.getSoBan()));
            i++;
        }
        TableModel model = new DefaultTableModel(data, column);
        this.jTable1.setModel(model);
    }

    public String laySoBan(String tenban) {
        int index = tenban.lastIndexOf(" ");
        index++;
        String a = "";
        for (int i = index; i < tenban.length(); i++) {
            a = a.concat(Character.toString(tenban.charAt(i)));
        }
        return a;
    }

