    private void luuBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_luuBTNActionPerformed
        // TODO add your handling code here:
        if (trangthai == 1) {
            String soban = this.sobanTXT.getText();

            Ban bn = new Ban();
            // soban=laySoBan(soban);
            try {
                int Soban = Integer.parseInt(soban);
                bn.setBan(Soban);
                if (svban.themBan(bn) != 0) {
                    JPanel Frame = new JPanel();
                    JOptionPane.showMessageDialog(Frame, "Thêm Bàn Thành Công");
                    duaDuLieuVaoTable();
                    this.trangthai = 0;
                    dis_or_en_ableButton();

                } else {
                    JPanel Frame = new JPanel();
                    JOptionPane.showMessageDialog(Frame, "Không Thể Thêm Được Số Bàn Đã Tồn Tại");
                }
            } catch (NumberFormatException e) {
                e.printStackTrace();
                JPanel Frame = new JPanel();
                JOptionPane.showMessageDialog(Frame, "Vui Lòng Nhập Vào Một Số");
            }

        }
        if (this.trangthai == 2) {

            String soban = this.sobanTXT.getText();
            int idban = Integer.parseInt(this.idTXT.getText());
            //   int soban =Integer.parseInt(soban1);
            Ban bn = new Ban();
            // soban=laySoBan(soban);
            try {
                int Soban = Integer.parseInt(soban);
                bn.setBan(Soban);
                bn.setId_ban(idban);
                if (svban.suaBan(bn) != 0) {
                    JPanel Frame = new JPanel();
                    JOptionPane.showMessageDialog(Frame, "Sửa Bàn Thành Công");
                    duaDuLieuVaoTable();
                    this.trangthai = 0;
                    dis_or_en_ableButton();

                } else {
                    JPanel Frame = new JPanel();
                    JOptionPane.showMessageDialog(Frame, "Không Thể Sửa Được Số Bàn Đã Tồn Tại");
                }
            } catch (NumberFormatException e) {
                e.printStackTrace();
                JPanel Frame = new JPanel();
                JOptionPane.showMessageDialog(Frame, "Vui Lòng Nhập Vào Một Số");
            }

        }

    }//GEN-LAST:event_luuBTNActionPerformed

