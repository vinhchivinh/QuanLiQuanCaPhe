    private void xoaBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_xoaBTNActionPerformed
        // TODO add your handling code here:
        Ban ban = new Ban();
        int index = this.jTable1.getSelectedRow();
        String value_cell_id = this.jTable1.getValueAt(index, 0).toString();
        String value_cell_soban = this.laySoBan(this.jTable1.getValueAt(index, 1).toString());
        this.idTXT.setText(value_cell_id);
        this.sobanTXT.setText(value_cell_soban);

        ban.setId_ban(Integer.parseInt(value_cell_id));
        JPanel frame = new JPanel();
        int kq = JOptionPane.showConfirmDialog(frame,
                "Bạn có chắc muốn xóa thức uống này",
                "Xác nhận",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE);
        if (kq == JOptionPane.YES_OPTION) {
            SVBan svb = new SVBan();

            if (svb.xoaBan(ban) == 0) {
                JOptionPane.showMessageDialog(frame, "Xóa không thành công");
            }
        }
        duaDuLieuVaoTable();

    }//GEN-LAST:event_xoaBTNActionPerformed

