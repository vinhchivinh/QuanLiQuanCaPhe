    private void suaBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_suaBTNActionPerformed
        // TODO add your handling code here:

        int index_row = this.jTable1.getSelectedRow();
        if (index_row != -1) {
            String value_cell_masanpham = (String) this.jTable1.getValueAt(index_row, 0);
            String value_cell_tensanpham = (String) this.jTable1.getValueAt(index_row, 1);
            String value_cell_giatien = (String) this.jTable1.getValueAt(index_row, 2);

            this.jTextField1.setText(value_cell_tensanpham);
            this.giaTXT.setText(value_cell_giatien);
            this.masanpham = value_cell_masanpham;

            this.trangthai = 2;
            dis_or_en_ableButton();
        } else {
            JPanel Frame = new JPanel();
            JOptionPane.showMessageDialog(Frame, "Vui Lòng Chọn Thức Uống");
        }


    }//GEN-LAST:event_suaBTNActionPerformed

    private void luuBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_luuBTNActionPerformed
        // TODO add your handling code here:
        String tenthucuong = this.jTextField1.getText().toString();
        String giatien = this.giaTXT.getText();
        long giatien1 = Long.parseLong(giatien);
        if (this.trangthai == 1) {
            GiaTien gia = new GiaTien();
            gia.setGiathucuong(giatien1);
            KhoaChinh key = new KhoaChinh("D:\\JAVASWING\\QuanLyBanHang\\JavaApplication10\\src\\index.txt");
            String khoachinh = key.taoKhoaChinh("TU");
            ThucUong tu = new ThucUong(khoachinh, tenthucuong, gia);
            if (svnThucUong.themThucUong(tu) != 0) {
                JPanel Frame = new JPanel();
                JOptionPane.showMessageDialog(Frame, "Thêm Thức Uống Thành Công");
                duaDuLieuVaoTable();
                this.jTextField1.setText("");
                this.jTextField1.setEditable(false);

                this.giaTXT.setText("");
                this.giaTXT.setEditable(false);

                this.suaBTN.setEnabled(true);
                this.xoaBTN.setEnabled(true);
                this.trangthai = 0;
                dis_or_en_ableButton();
            } else {
                JPanel Frame = new JPanel();
                JOptionPane.showMessageDialog(Frame, "Không Thể Thêm Được");
            }
        }
        if (this.trangthai == 2) {

            String mathucuong = this.masanpham.toString();
            String gia = this.giaTXT.getText();
            GiaTien gia1 = new GiaTien();
            gia1.setGiathucuong(giatien1);
            // String tenthucuong =this.jTextField1.getText();
            ThucUong TU = new ThucUong();
            TU.setMathucuong(mathucuong);
            TU.setTenthucuong(tenthucuong);
            TU.setGia(gia1);

            if (svnThucUong.capNhatThucUong(TU) != 0) {
                JPanel Frame = new JPanel();
                JOptionPane.showMessageDialog(Frame, "Cập Nhật Thức Uống Thành Công");
                duaDuLieuVaoTable();
                this.jTextField1.setText("");
                this.jTextField1.setEditable(false);

                this.giaTXT.setText("");
                this.giaTXT.setEditable(false);

                this.suaBTN.setEnabled(true);
                this.xoaBTN.setEnabled(true);
                this.themBTN.setEnabled(true);
                this.themBTN.setEnabled(true);
                this.trangthai = 0;
                dis_or_en_ableButton();
            } else {
                JPanel Frame = new JPanel();
                JOptionPane.showMessageDialog(Frame, "Không Thể Cập Nhật");
            }
        }


    }//GEN-LAST:event_luuBTNActionPerformed

