    public void duaDuLieuVaoTable() {
        this.jTextField1.setEditable(false);
        List<ThucUong> danhsachthucuong = new ArrayList<>();
        SVThucUong svt = new SVThucUong();
        danhsachthucuong = svt.danhSachThucUong();
        String[] column = {"Mã Thức Uống", "Tên Thức Uống", "Giá"};
        String[][] data = new String[1000][1000];
        int i = 0, j = 0;
        for (ThucUong values : danhsachthucuong) {
            data[i][j] = values.getMathucuong();
            data[i][j + 1] = values.getTenthucuong();
            data[i][j + 2] = Long.toString(values.getGia().getGiathucuong());
            i++;
        }

//        String[][] data = {
//            {"ABCDEFGHIJKLMNOPQRSTUVWXYZ","Con Chó"},
//        };
        TableModel model = new DefaultTableModel(data, column);
        this.jTable1.setModel(model);
    }

    private void dis_or_en_ableButton() {
        if (trangthai == 1) {
            this.jTextField1.setEditable(true);
            this.giaTXT.setEditable(true);
            this.suaBTN.setEnabled(false);
            this.xoaBTN.setEnabled(false);
            this.themBTN.setEnabled(true);
            this.luuBTN.setEnabled(true);
        } else if (trangthai == 2) {
            this.suaBTN.setEnabled(true);
            this.themBTN.setEnabled(false);
            this.xoaBTN.setEnabled(false);
            this.jTextField1.setEditable(true);
            this.giaTXT.setEditable(true);
            this.luuBTN.setEnabled(true);
        } else {
            this.suaBTN.setEnabled(true);
            this.themBTN.setEnabled(true);
            this.xoaBTN.setEnabled(true);
            this.jTextField1.setEditable(true);
            this.giaTXT.setEditable(true);
            this.luuBTN.setEnabled(false);
        }
    }

