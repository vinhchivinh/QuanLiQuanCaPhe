    void themDuLieuVaoTable(String a[][], String b, String c, String d, String e) {
        a[demSoPhanTuTrongMang(a)][0] = b;
        a[demSoPhanTuTrongMang(a) - 1][1] = c;
        a[demSoPhanTuTrongMang(a) - 1][2] = d;
        a[demSoPhanTuTrongMang(a) - 1][3] = e;
    }

    public void duaDuLieuVaoTable() {
        String[] column = {"SỐ BÀN", "TRẠNG THÁI"};
        List<Ban> danh_sach_ban = new ArrayList<>();
        SVBan ban = new SVBan();
        danh_sach_ban = ban.danhsachban_hoadon();
        int i = 0;
        String trangthai;
        for (Ban value : danh_sach_ban) {
            this.bangban[i][0] = Integer.toString(value.getSoBan());
            if (value.getHoadon().getTrangthai() == 1) {
                trangthai = "Đang sử dụng";
            } else {
                trangthai = "Trống";
            }
            this.bangban[i][1] = trangthai;
            this.bangban[i][2] = Integer.toString(value.getId_ban());
            this.bangban[i][3] = value.getHoadon().getMahoadon();
            i = i + 1;
        }
        TableModel model = new DefaultTableModel(bangban,column);
        this.banTable.setModel(model);
    }

