    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
      
        this.bangchitiethoadon =new String[1000][10];
        int index = this.banTable.getSelectedRow();
        List<ChiTietHoaDon> cthd = new ArrayList<>();
        SVHoaDon svh = new SVHoaDon();
        cthd = svh.layChiTietHoaDonTheoBan(Integer.parseInt(this.bangban[index][2]));
        

        int stt = 1;
        int i = 0;
        for (ChiTietHoaDon values : cthd) {
            this.bangchitiethoadon[i][0] = Integer.toString(stt);
            this.bangchitiethoadon[i][1] = values.getThucuong().getTenthucuong();
            this.bangchitiethoadon[i][2] = Long.toString(values.getThucuong().getGia().getGiathucuong());
            this.bangchitiethoadon[i][3] = Integer.toString(values.getSl());
            this.bangchitiethoadon[i][4] = Long.toString(values.getThucuong().getGia().getGiathucuong() * values.getSl());
            this.bangchitiethoadon[i][5] = values.getThucuong().getMathucuong();
            i++;
            stt++;
        }
        String[] column ={"STT","Tên Thức Uống","Giá Tiền","Số Lượng","Thành Tiền"};
        
        TableModel model = new DefaultTableModel(this.bangchitiethoadon, column);
        this.chitiethoadonTB.setModel(model);
        if(this.demSoPhanTuTrongMang(this.bangchitiethoadon)==0){
            this.xacnhanBTN.setEnabled(false);
        }
        else {
             this.xacnhanBTN.setEnabled(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

